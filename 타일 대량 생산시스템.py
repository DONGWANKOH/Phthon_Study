# [문제]
# 크기가 다른 타일을 대량으로 생산하는 타일대량생산 시스템이 있습니다. 이 시스템은 다음과 같이 작동합니다.
#
# 생산을 시작하면, 현재 대기 중인 주문 중 가장 작은 타일을 하나 생산합니다.
# 생산한 타일을 주문한 고객에게 배송합니다.
# 생산한 타일이 없는 경우, 새로운 주문이 들어올 때까지 대기합니다.
# 타일의 크기와 주문 정보가 주어질 때, 타일대량생산 시스템이 생산하는 모든 타일을 배송하는 데 걸리는 시간을 구하는 프로그램을 작성하세요.
#
# [입력]
# 첫 번째 줄에는 타일의 크기인 정수 s가 주어집니다(1 ≤ s ≤ 1,000). 두 번째 줄에는 주문의 개수인 정수 n이 주어집니다(1 ≤ n ≤ 1,000). 그 다음 줄부터 n개의 줄에 걸쳐 각 주문에 대한 정보가 주어집니다. 각 줄은 "주문번호 타일크기"의 형식으로 주어지며, 타일 크기는 s 이하의 자연수입니다.
#
# [출력]
# 모든 타일을 배송하는 데 걸리는 시간을 출력합니다.
#
# [예제 입력]
# 3
# 6
# 1 2
# 2 1
# 3 3
# 4 2
# 5 1
# 6 2
#
# [예제 출력]
# 10
#
# [힌트]
# 주문번호 1, 5, 2, 4, 6, 3 순으로 생산하면 총 10초가 걸립니다.


# 처음에는 입력으로 받은 타일 크기와 주문 번호를 따로 저장해두었다가,
# 이를 오름차순으로 정렬하여 타일을 생산하는 순서대로 배치합니다. 그리고 배치된 순서대로 생산을 진행하며 걸리는 시간을 계산해주면 됩니다.
#
# 주문 번호는 출력하지 않아도 되므로, 타일 크기만 저장해주었습니다. 이렇게 구현하면 작은 크기부터 생산하는 것을 보다 쉽게 구현할 수 있습니다.


tile_size = int(input())
n = int(input())
orders = []

for i in range(n):
    order_num, size = map(int, input().split())
    orders.append((size, order_num))

# 오름차순으로 정렬합니다.
orders.sort()

time = 0
for size, order_num in orders:
    time += size
print(time)
